namespace StampService.Domain.Access;

public enum PermissionCode
{
    /// <summary>
    /// Управление брендом: настройки, базовые данные и административные действия уровня бренда.
    /// </summary>
    BrandManage = 1,

    /// <summary>
    /// Управление метриками лояльности бренда: создание, изменение, активация и деактивация.
    /// </summary>
    MetricManage = 20,

    /// <summary>
    /// Управление участниками команды бренда: назначение, удаление и изменение ролей сотрудников.
    /// </summary>
    StaffManage = 40,

    /// <summary>
    /// Начисление штампов или баллов клиентам в рамках бренда.
    /// </summary>
    StampIssue = 60,

    /// <summary>
    /// Просмотр балансов клиентов по метрикам лояльности бренда.
    /// </summary>
    BalanceView = 80
}
